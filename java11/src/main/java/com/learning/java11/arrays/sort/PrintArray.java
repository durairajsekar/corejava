package com.learning.java11.arrays.sort;

public class PrintArray {
	
	static void input(int[] inputArray) {
		System.out.println("Input recieved:");
		for (var t : inputArray) {
			System.out.print(t);
			System.out.print("\n");
		}
	}

	static void output(int[] outputArray) {
		System.out.println("Output:");
		for (var t : outputArray) {
			System.out.print(t);
			System.out.print("\n");
		}
	}

}
